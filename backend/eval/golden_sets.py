"""Frozen golden-set fixtures for the AI eval harness (Roadmap P1-3).

Each fixture below represents ~5 "decks" worth of hand-verified expectations
across the three measurable quality dimensions the roadmap calls out. These
are deliberately small, synthetic, and self-contained (no real PDFs/uploads
needed) — the point is a stable, human-verified expected answer to compare
the pipeline's actual output against, not deck realism.

Extending the golden set: add a case to the relevant list below. Each case
must have its `expected_*` field verified by a human, not generated by the
model under test — a golden set that grades itself against its own model's
opinion isn't a regression detector.
"""
from __future__ import annotations

from dataclasses import dataclass
from typing import List, Set


@dataclass(frozen=True)
class QuizGoldenCase:
    """One human-verified quiz-question answer key."""
    deck_id: str
    slide_index: int
    question: str
    options: List[str]
    expected_answer_index: int  # human-verified correct option


@dataclass(frozen=True)
class TutorFaithfulnessCase:
    """A student question paired with the slide(s) that actually ground a
    correct answer, per human review of the source deck."""
    deck_id: str
    question: str
    expected_grounded_slide_indices: Set[int]


@dataclass(frozen=True)
class RetrievalCase:
    """A search/tutor query paired with the slides a human judged relevant,
    for precision@k scoring."""
    deck_id: str
    query: str
    expected_relevant_slide_indices: Set[int]
    k: int = 5


